void solve(){
  ll a,b;cin >> a >> b;
  while(b%a){
    ll x = b/a;
    cout << 1 << "/" << x + 1 << " + ";
    a = (x+1)*a - b;
    b *= (x+1);
  }
  cout << 1 << "/" << b/a << "\n";
}

