ll deg2[50];
ll lt(ll a,ll b){
  ll res = 1;
  while(b){
    if(b%2) res*=a;
    a*=a;
    b/=2; 
  }
  return res;
}
void sinh(){
  for(int i=0;i<=30;++i){
    deg2[i] = lt(2,(ll)i);
  }
}
ll tong(int n,int a[]){
  ll res = 0;
  for(int i=0;i<=n;++i) res+=a[i]*deg2[i];
  return res;
}
