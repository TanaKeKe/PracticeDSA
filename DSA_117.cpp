string s;
int n,a[100],visited[100];
void in(){
    for(int i=1;i<=n;++i) cout << s[a[i]-1];
    cout << " ";
}
void Try(int i){
    for(int j =1;j<=n;++j){
        if(visited[j] == 0){
            visited[j] = 1;
            a[i] = j;
            if(i==n) in();
            else Try(i+1);
            visited[j] = 0;
        }
    }
}
void solve(){
  cin >> s;
  n = s.size();
  Try(1);
  cout << "\n";
}

