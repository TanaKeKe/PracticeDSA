bool visited[1001];
void BFS(vector<int> adj[],int u){
  queue<int> q;
  q.push(u);
  cout << u << " ";
  visited[u] = true;
  while(!q.empty()){
    int k =q.front();
    q.pop();
    for(int i=0;i<adj[k].size();++i){
      if(visited[adj[k][i]] == false){
        cout << adj[k][i] << " ";
        visited[adj[k][i]] = true;
        q.push(adj[k][i]);
      }
    }
  }
}
void solve(){
  for(int i=1;i<=1000;++i) visited[i] = false;
  int v,e,u;cin >> v >> e >> u;
  vector<int> adj[1001];
  for(int i=1;i<=e;++i){
    int x;cin >> x;
    int y;cin >> y;
    adj[x].push_back(y);
    adj[y].push_back(x);
  }

  BFS(adj,u);
  cout << "\n";
}

