int cdMin(int n,int m){
  string n1 = to_string(n);
  string m1 = to_string(m);
  for(int i=0;i<n1.size();++i){
    if(n1[i] == '6') n1[i] = '5';
  }

  for(int i=0;i<m1.size();++i){
    if(m1[i] == '6') m1[i] = '5';
  }

  n = stoi(n1);
  m = stoi(m1);
  return n+m;
}

int cdMax(int n,int m){
  string n1 = to_string(n);
  string m1 = to_string(m);
  for(int i=0;i<n1.size();++i){
    if(n1[i] == '5') n1[i] = '6';
  }

  for(int i=0;i<m1.size();++i){
    if(m1[i] == '5') m1[i] = '6';
  }

  n = stoi(n1);
  m = stoi(m1);
  return n+m;
}
void solve(){
	int n,m;cin >> n >> m;
  cout << cdMin(n,m) << " " << cdMax(n,m);
}

