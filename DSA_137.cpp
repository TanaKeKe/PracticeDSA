int visited[1001];
void BFS(int u){
    visited[u] = 1;
    queue<int> q;
    q.push(u);
    while(!q.empty()){
        int s = q.front();
        q.pop();
        for(int i=0;i<adj[s].size();++i){
            if(visited[adj[s][i]] == 0){
                q.push(adj[s][i]);
                visited[adj[s][i]] =1;
            }
        }
    }
}
