void solve(){
  int n,k;cin >> n >> k;
  int a[k+5];
  for(int i=1;i<=k;++i) cin >> a[i];
    map<int,int> mp;
  for(int i=1;i<=k;++i) mp[a[i]]++;
  int i = k;
  while(i>0 && a[i] == n-k+i) --i;

  if(i==0) cout << k << "\n";
  else{
    ++a[i];
    for(int j =i+1;j<=k;++j) a[j] = a[j-1] + 1;
  }
  int cnt = 0;
  for(int z=1;z<=k;++z) if(mp[a[z]] == 1) ++cnt;
  if(i!=0) cout << k-cnt << "\n";
}

