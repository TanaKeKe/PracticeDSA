deque<int> dq;
void solve(){
  string s; cin >> s;
  if(s == "PUSHFRONT"){
    int x;cin >> x;
    dq.push_front(x);
  }
  else if(s=="PRINTFRONT"){
    if(dq.empty()) cout << "NONE\n";
    else cout << dq.front() << "\n";
  } else if(s == "POPFRONT") {
    if(!dq.empty()) dq.pop_front();
  } else if(s == "PUSHBACK"){
    int x;cin >> x;
    dq.push_back(x);
  } else if(s == "PRINTBACK"){
    if(dq.empty()) cout << "NONE\n";
    else cout << dq.back() << "\n";
  } else {
    if(!dq.empty()) dq.pop_back();
  }
}

