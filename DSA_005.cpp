ll result=-1e9,sum;
void solve(){
  result=-1e9;
	cin >> n;
  for(int i=1;i<=n;++i){
    a[i]=i;
  }
  for(int i=1;i<=n;++i){
    for(int j=1;j<=n;++j) cin >> value[i][j];
  }

  do {
    sum=0;
    for(int i =1;i<=n;++i) sum += value[i][a[i]];
    if(result < sum){
      result = sum;
      /*for(int i=1;i<=n;++i) cout << a[i] << " ";
      cout << "|" << result << "\n";*/
    }
  } while(next_permutation(a+1,a+n+1));

  cout << result << "\n";
}

