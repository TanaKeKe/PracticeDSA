int n,s;
int a[100];
int ans,res;
void Try(int i,int k,int res){
  if(k==0){
    ans = min(ans,res);
    return;
  }
  if(k<0 || i>=n || res >= ans) return;
  Try(i+1,k-a[i],res+1);
  Try(i+1,k,res);
}
void solve(){
  cin >> n >> s;
  for(int i =0;i<n;++i) cin >> a[i];
    sort(a,a+n,greater<int>());
  Try(0,s,0);
  cout << ans;
}

