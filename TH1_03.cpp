int n,m,cnt,a[100],visited[100];
void in(){
  ++cnt;
  if(cnt%m==0) {
    for(int i=1;i<=n;++i) cout << a[i] << " ";
    cout << "\n";
  }
}
void Try(int i){
  for(int j=1;j<=n;++j){
    if(visited[j] == 0){
      a[i] = j;
      visited[j] = 1;
      if(i==n){
        in();
      }
      else Try(i+1);
      visited[j] = 0;
    }
  }
}
void solve(){
  cin >> n >> m;
  Try(1);
}

